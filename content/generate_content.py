import itertools

def make_schedule():
    """
    Day 8-30 (23일) 스케줄.
    7개 포스트/일 — 카테고리 순환 + 세일즈 2/7 비율
    """
    hooks_cycle     = itertools.cycle(HOOKS)
    love_cycle      = itertools.cycle(LOVE)
    career_cycle    = itertools.cycle(CAREER)
    healing_cycle   = itertools.cycle(HEALING)
    sales_cycle     = itertools.cycle(SALES)
    edu_cycle       = itertools.cycle(EDUCATION)
    engage_cycle    = itertools.cycle(ENGAGEMENT)

    # 요일별 주제 강조 (0=월, 6=일)
    # Day 8 = 월요일 기준으로 잡음
    themes = [
        # (post1,   post2,    post3,    post4,     post5,    post6,    post7)
        ("hook",  "career", "edu",    "hook",    "sales",  "love",   "engage"),   # 월
        ("love",  "hook",   "career", "healing", "sales",  "edu",    "engage"),   # 화
        ("hook",  "edu",    "love",   "career",  "sales",  "healing","engage"),   # 수
        ("career","hook",   "love",   "edu",     "sales",  "healing","engage"),   # 목
        ("hook",  "love",   "career", "sales",   "edu",    "healing","engage"),   # 금
        ("love",  "hook",   "engage", "sales",   "healing","edu",    "career"),   # 토
        ("healing","hook",  "edu",    "love",    "sales",  "career", "engage"),   # 일
    ]

    category_map = {
        "hook":    hooks_cycle,
        "love":    love_cycle,
        "career":  career_cycle,
        "healing": healing_cycle,
        "sales":   sales_cycle,
        "edu":     edu_cycle,
        "engage":  engage_cycle,
    }

    days = []
    for d in range(23):  # Day 8 ~ Day 30
        day_num = d + 8
        weekday = d % 7
        theme = themes[weekday]
        posts = []
        for i, cat in enumerate(theme):
            name, content = next(category_map[cat])
            posts.append((i+1, cat, name, content))
        days.append((day_num, posts))
    return days


def write_day(day_num, posts, base_dir):
    week = ((day_num - 1) // 7) + 1
    out_dir = os.path.join(base_dir, f"week{week}")
    os.makedirs(out_dir, exist_ok=True)
    fname = os.path.join(out_dir, f"day{day_num:02d}.md")

    weekday_names = ["월요일","화요일","수요일","목요일","금요일","토요일","일요일"]
    weekday = (day_num - 1) % 7  # Day 1 = 월요일 기준

    lines = [f"## DAY {day_num:02d} — {weekday_names[weekday]}\n\n"]
    for idx, cat, name, content in posts:
        lines.append(f"### POST {idx:02d} ({cat.upper()} / {name})\n---\n{content}\n---\n\n")

    with open(fname, "w", encoding="utf-8") as f:
        f.write("".join(lines))
    return fname


def main():
    base_dir = os.path.dirname(os.path.abspath(__file__))
    schedule = make_schedule()
    created = []
    for day_num, posts in schedule:
        fname = write_day(day_num, posts, base_dir)
        created.append(fname)
        print(f"✅ {os.path.basename(fname)} 생성 ({len(posts)}개 포스트)")

    print(f"\n🎉 완료! 총 {len(created)}개 파일 / {len(created)*7}개 포스트 생성")
    print(f"📁 경로: {base_dir}")


if __name__ == "__main__":
    main()
